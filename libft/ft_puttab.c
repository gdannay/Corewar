#include "libft.h"

void	ft_puttab(char **tab, int endl)
{
	int	i;

	i = 0;
	while (tab[i])
	{
		ft_putstr(tab[i]);
		if (endl == 1)
			ft_putchar('\n');
		i++;
	}
}
